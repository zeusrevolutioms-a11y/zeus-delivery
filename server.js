//----------------------------------------------------------------------------------------------------------------------
// 🚀 ROTA SPEED - SERVER
// Backend simples para rastreamento de motoboys
//----------------------------------------------------------------------------------------------------------------------

const express = require('express');
const cors = require('cors');

const app = express();

//----------------------------------------------------------------------------------------------------------------------
// ⚙️ CONFIG
//----------------------------------------------------------------------------------------------------------------------

app.use(express.json());
app.use(cors());
app.use(express.static('public')); // 👈 serve HTML (admin + motoboy)

//----------------------------------------------------------------------------------------------------------------------
// 📦 MEMÓRIA (futuro: banco de dados)
//----------------------------------------------------------------------------------------------------------------------

let motoboys = {};

//----------------------------------------------------------------------------------------------------------------------
// 📍 RECEBER LOCALIZAÇÃO DO MOTOBOY
//----------------------------------------------------------------------------------------------------------------------

app.post('/location', (req, res) => {
    const { id, lat, lng } = req.body;

    if (!id) {
        return res.status(400).send({ error: 'ID obrigatório' });
    }

    motoboys[id] = {
        lat,
        lng,
        updated_at: new Date()
    };

    res.send({ status: 'ok' });
});

//----------------------------------------------------------------------------------------------------------------------
// 📡 LISTAR TODOS OS MOTOBOYS
//----------------------------------------------------------------------------------------------------------------------

app.get('/motoboys', (req, res) => {
    res.send(motoboys);
});

//----------------------------------------------------------------------------------------------------------------------
// 🚪 START SERVER
//----------------------------------------------------------------------------------------------------------------------

app.listen(process.env.PORT || 3000, () => {
    console.log('🔥 ROTA SPEED rodando');
});
